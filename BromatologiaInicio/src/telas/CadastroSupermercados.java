    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        // TODO add your handling code here:
        Supermercado supermercado =  new Supermercado();
        supermercado.setCUIT_CUIL(txtCUIT.getText());
        supermercado.setNombre(txtNombre.getText());
        supermercado.setTelefone(txtTelefone.getText());
        supermercado.setUsuario(txtUsuario.getText());
        supermercado.setSenha(txtSenha.getText());
        supermercado.setTipo_User(Integer.parseInt(txtTipo_User.getText()));

        SupermercadoDao supermercadoDao = new SupermercadoDao();
        supermercadoDao.inserir(supermercado);
        this.dispose();
        PagInicioLogin telaLogin = new PagInicioLogin();
        telaLogin.setVisible(true);
        
    }//GEN-LAST:event_btnAdicionarActionPerformed

    
    
